[BITS 32]
[EXTERN isr_handler]
[EXTERN irq_handler]

[GLOBAL gdt_flush]
[GLOBAL idt_flush]

gdt_flush:
    mov eax, [esp + 4]
    lgdt [eax]
    mov ax, 0x10
    mov ds, ax
    mov es, ax
    mov fs, ax
    mov gs, ax
    mov ss, ax
    jmp 0x08:.flush
.flush:
    ret

idt_flush:
    mov eax, [esp + 4]
    lidt [eax]
    ret

%macro ISR_NOERR 1
